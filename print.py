#Uso de coma en print->Se usa para separar varios argumentos
print("nunca", "pares", "de", "aprender")
#Concatenacion
print("Nunca"+"pares"+"de"+"aprender")
#Concatenacion con espacios
print("Nunca" + " " + "pares" + " " + "de" + " " +  "apreder" )
#Uso de sep
print("nunca", "pares", "de", "aprender", sep="8 " )
#Uso de end inprime en la misma linea
print("Nunca", end=" ")
print("pares de aprender (END")